def generic_hi(name='world'):
    """ Says Hello, Name! - default name is world"""
    return f'Hello, {name}!'    

def reversing(listA):
    """ Reverses a list of integers"""
    return listA[::-1]

def is_multiple(n,m):
    """Returns whether or not n is a multiple of m"""
    return n % m == 0

